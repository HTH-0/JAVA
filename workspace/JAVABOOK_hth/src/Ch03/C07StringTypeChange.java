package Ch03;

public class C07StringTypeChange {
	public static void main(String[] args) {
		
		
		// 문자열 + 문자열
		
		System.out.println("문자열1" + "문자열2");
		System.out.println("문자열1" + "문자열2");
		System.out.println("문자열1" + ',' + "문자열2");
		System.out.println("문자열1" + 2);
		System.out.println("문자열1" + ',' + '!');
		System.out.println( ',' + '!' + "문자열1");
		System.out.println("문자열1" + "");
		
		
		
		
		
	}
}
